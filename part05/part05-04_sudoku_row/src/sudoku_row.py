def column_correct(sudoku: list, row_no: int):
    flag = True
    for elem in sudoku[row_no]:
        if elem == 0:
            continue
        else:
            counter = sudoku[row_no].count(elem)
            if counter > 1:
                flag = False
                break
    return flag
        
